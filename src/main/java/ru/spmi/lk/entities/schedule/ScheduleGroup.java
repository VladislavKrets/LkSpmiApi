package ru.spmi.lk.entities.schedule;

public class ScheduleGroup {
    private int id;
    private String label;
    private String description;
    private String type;

    public int getId() {
        return id;
    }

    public String getLabel() {
        return label;
    }

    public String getDescription() {
        return description;
    }

    public String getType() {
        return type;
    }
}
