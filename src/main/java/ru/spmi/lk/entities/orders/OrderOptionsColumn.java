package ru.spmi.lk.entities.orders;

public class OrderOptionsColumn {
    private String code;
    private String name;
    private int show;
    private String field;
    private int sort;
    private String type;

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public int getShow() {
        return show;
    }

    public String getField() {
        return field;
    }

    public int getSort() {
        return sort;
    }

    public String getType() {
        return type;
    }
}
